module ShoplistsHelper
  def convert_units(a, b)
    case a
    
    when 'pinch'
    when 'dash'
    when 'tsp'
    when 'tbsp'
    when 'cup'
    when 'pound'
    when 'fl oz'
    when 'pint'
    when 'quart'
    when 'gallon'
    
    end
  end
  
  
  def convert_to_b(a, b)
    case b
    when 'pinch'
    when 'dash'
    when 'tsp'
    when 'tbsp'
    when 'cup'
    when 'pound'
    when 'fl oz'
    when 'pint'
    when 'quart'
    when 'gallon'
    end
  end
end
